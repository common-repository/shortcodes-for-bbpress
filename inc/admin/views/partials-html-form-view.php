	<div style="padding: 70px;">
		<h2>
		
	bbPress Shortcodes</h2>
<br>
<br>
<strong>Forums</strong>
<br>
<br><strong>[bbp-forum-index]</strong> – This will display your entire forum index.
<br>
<br><strong>[bbp-forum-form]</strong> – Display the ‘New Forum’ form.
<br>
<br><strong>[bbp-single-forum id=$forum_id]</strong> – Display a single forums topics. eg. <strong>[bbp-single-forum id=32]</strong>
<br>
<br><br>
<br>
<strong>Topics</strong>
<br>
<br><strong>[bbp-topic-index]</strong> – Display the most recent 15 topics across all your forums with pagination.
<br>
<br><strong>[bbp-topic-form]</strong> – Display the ‘New Topic’ form where you can choose from a drop down menu the forum that this topic is to be associated with.
<br>
<br><strong>[bbp-topic-form forum_id=$forum_id]</strong> – Display the ‘New Topic Form’ for a specific forum ID.
<br>
<br><strong>[bbp-single-topic id=$topic_id]</strong> – Display a single topic. eg. <strong>[bbp-single-topic id=4096]</strong>

<br>
<br><br>
<br><strong>Replies</strong>
<br>
<br><strong>[bbp-reply-form]</strong> – Display the ‘New Reply’ form.
<br>
<br><strong>[bbp-single-reply id=$reply_id]</strong> – Display a single reply eg. <strong>[bbp-single-reply id=32768]</strong>

<br>
<br><br>
<br><strong>Topic Tags</strong>
<br>
<br><strong>[bbp-topic-tags]</strong> – Display a tag cloud of all topic tags.
<br>
<br><strong>[bbp-single-tag id=$tag_id]</strong> – Display a list of all topics associated with a specific tag. eg. <strong>[bbp-single-tag id=64]</strong>

<br>
<br><strong>Views</strong>
<br>
<br><strong>[bbp-single-view]</strong> – Single view – Display topics associated with a specific view. Current included ‘views’ with bbPress are “popular” <strong>[bbp-single-view id=’popular’]</strong> and “No Replies” <strong>[bbp-single-view id=’no-replies’]</strong>

<br>
<br><br>
<br><strong>Search</strong>
<br>
<br><strong>[bbp-search]</strong> – Display the search input form.
<br>
<br><strong>[bbp-search-form]</strong> – Display the search form template.

<br>
<br><br>
<br><strong>Account</strong>
<br>
<br><strong>[bbp-login]</strong> – Display the login screen.
<br>
<br><strong>[bbp-register]</strong> – Display the register screen.
<br>
<br><strong>[bbp-lost-pass]</strong> – Display the lost password screen.
<br>
<br>
<br>
<br><strong>Statistics</strong>
<br>
<br><strong>[bbp-stats]</strong> – Display the forum statistics.
		
		
		
		
